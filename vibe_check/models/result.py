"""ScanResult model — SHARED contract, frozen at minute 0."""

from __future__ import annotations

import json
from dataclasses import dataclass, field
from typing import List

from vibe_check.models.finding import Finding, Severity


@dataclass
class ScanResult:
    """Complete result from a vibe-check scan."""
    score: float
    grade: str
    verdict: str
    findings: List[Finding]
    category_scores: dict
    scan_time: float
    repo_path: str
    languages_detected: List[str] = field(default_factory=list)
    files_scanned: int = 0
    tokens_used: int = 0

    # ── Serialization ──────────────────────────────────────────────

    def to_json(self) -> str:
        """Full JSON export."""
        return json.dumps(
            {
                "score": round(self.score, 1),
                "grade": self.grade,
                "verdict": self.verdict,
                "scan_time": round(self.scan_time, 2),
                "repo_path": self.repo_path,
                "languages_detected": self.languages_detected,
                "files_scanned": self.files_scanned,
                "tokens_used": self.tokens_used,
                "category_scores": {
                    k: round(v, 1) for k, v in self.category_scores.items()
                },
                "findings_count": len(self.findings),
                "findings": [f.to_dict() for f in self.findings],
            },
            indent=2,
        )

    def to_markdown(self) -> str:
        """GitHub PR comment markdown."""
        sev_emoji = {
            Severity.CRITICAL: "🔴",
            Severity.HIGH: "🟠",
            Severity.MEDIUM: "🟡",
            Severity.LOW: "🔵",
            Severity.INFO: "⚪",
        }

        lines = [
            f"## 🔍 VibeAudit Report",
            f"",
            f"**Score:** {self.score:.0f}/100 ({self.grade}) — {self.verdict}",
            f"",
            f"| Metric | Value |",
            f"|--------|-------|",
            f"| Files Scanned | {self.files_scanned} |",
            f"| Languages | {', '.join(self.languages_detected) or 'N/A'} |",
            f"| Scan Time | {self.scan_time:.1f}s |",
            f"| Findings | {len(self.findings)} |",
            f"| LLM Tokens | {self.tokens_used} |",
            f"",
        ]

        # Category scores table
        if self.category_scores:
            lines.append("### Category Scores")
            lines.append("")
            lines.append("| Category | Score |")
            lines.append("|----------|-------|")
            for cat, sc in sorted(
                self.category_scores.items(), key=lambda x: x[1]
            ):
                bar = "🟩" * int(sc // 10) + "⬜" * (10 - int(sc // 10))
                lines.append(f"| {cat.replace('_', ' ').title()} | {sc:.0f} {bar} |")
            lines.append("")

        # Critical/High findings
        critical_high = [
            f
            for f in self.findings
            if f.severity in (Severity.CRITICAL, Severity.HIGH)
        ]
        if critical_high:
            lines.append("### 🚨 Critical & High Findings")
            lines.append("")
            for f in critical_high:
                emoji = sev_emoji.get(f.severity, "⚪")
                loc = f"{f.file}:{f.line}" if f.file and f.line else (f.file or "—")
                lines.append(f"<details>")
                lines.append(f"<summary>{emoji} <b>{f.title}</b> — <code>{loc}</code></summary>")
                lines.append(f"")
                lines.append(f"**Severity:** {f.severity.value.upper()}  ")
                lines.append(f"**Description:** {f.description}  ")
                lines.append(f"**Remediation:** {f.remediation}  ")
                if f.ai_prompt:
                    lines.append(f"")
                    lines.append(f"**Cursor/Copilot Prompt:**")
                    lines.append(f"```")
                    lines.append(f"{f.ai_prompt}")
                    lines.append(f"```")
                lines.append(f"</details>")
                lines.append(f"")

        # Summary counts
        counts = {}
        for f in self.findings:
            counts[f.severity.value] = counts.get(f.severity.value, 0) + 1
        if counts:
            lines.append("### Summary")
            lines.append("")
            for sev in ["critical", "high", "medium", "low", "info"]:
                if sev in counts:
                    emoji = sev_emoji.get(Severity(sev), "⚪")
                    lines.append(f"- {emoji} **{sev.upper()}**: {counts[sev]}")
            lines.append("")

        lines.append("---")
        lines.append("*Generated by [VibeAudit](https://github.com/vibe-check) 🎵*")
        return "\n".join(lines)
